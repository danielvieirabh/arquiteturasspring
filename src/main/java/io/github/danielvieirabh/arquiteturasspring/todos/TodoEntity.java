package io.github.danielvieirabh.arquiteturasspring.todos;

import jakarta.persistence.*;

@Entity
@Table(name = "tb_todo")
public class TodoEntity {
    @Id
    @Column(name = "id") //Essa anotation nao e obrigatoria
    @GeneratedValue(strategy = GenerationType.IDENTITY) //Cria um valor altomatico para o id , autoincremento
    private Integer id;
    @Column(name = "descricao")
    private String descricao;
    @Column(name = "fl_concluido")
    private Boolean concluido;

    public TodoEntity() {
    }

    public TodoEntity(Integer id, String descricao, Boolean concluido) {
        this.id = id;
        this.descricao = descricao;
        this.concluido = concluido;
    }

    public Boolean getConcluido() {
        return concluido;
    }

    public void setConcluido(Boolean concluido) {
        this.concluido = concluido;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
